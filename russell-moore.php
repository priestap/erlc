					<h1 class="page-title">Russell Moore</h1>
					<p class="lead">Russell Moore serves as the eighth president of the Ethics &amp; Religious Liberty Commission of the Southern Baptist Convention, the moral and public policy agency of the nation’s largest Protestant denomination.

					<p><img class="img-responsive photo pull-right" src="http://erlc.com/images/article_photos/%23RussellDMoore_standard%20small.jpg" alt="Photo of Russell Moore" style="max-width: 33%;">Prior to his election to this role in 2013, Moore served as provost and dean of The Southern Baptist Theological Seminary, where he also taught as professor of theology and ethics.</p>

					<p>A widely-sought cultural commentator, Dr. Moore has been recognized by a number of influential organizations. The <i>Wall Street Journal</i> has called him “vigorous, cheerful, and fiercely articulate” while <i>The Gospel Coalition</i> has referred to him “one of the most astute ethicists in contemporary evangelicalism.”</p>

					<p>Dr. Moore blogs frequently at his <a href="http://www.russellmoore.com">Moore to the Point</a> website, and hosts a program called Questions &amp; Ethics—a wide-ranging podcast in which Dr. Moore answers listener-generated questions on the difficult moral and ethical issues of the day. In addition, he is the author of several books, including <i><a href="http://www.amazon.com/gp/product/1433515806/ref=as_li_qf_sp_asin_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=1433515806&amp;linkCode=as2&amp;tag=familyboonet-20">Tempted and Tried: Temptation and the Triumph of Christ</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=familyboonet-20&amp;l=as2&amp;o=1&amp;a=1433515806" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></i> and <i><a href="http://www.amazon.com/gp/product/1581349114/ref=as_li_qf_sp_asin_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=1581349114&amp;linkCode=as2&amp;tag=familyboonet-20">Adopted for Life: The Priority of Adoption for Christian Families &amp; Churches</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=familyboonet-20&amp;l=as2&amp;o=1&amp;a=1581349114" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></i>.</p>

					<p>An ordained minister, Dr. Moore has served as a pastor for a number of Southern Baptist churches—most recently serving as preaching pastor at Highview Baptist Church from 2008-2012. A native Mississippian, he and his wife Maria are the parents of five boys.</p>
			

